def choose_vacation():
  """Функция для выбора места для отдыха"""

  vacation_type = input("Предпочитаете ли вы активный или расслабляющий отдых? (активный/расслабляющий): ")

  if vacation_type.lower() == "активный":
    destination = input("Выберите направление: горы, лес или пустыня? ")
    if destination.lower() == "горы":
      print("Вы выбрали горы! Не забудьте взять теплую одежду и треккинговые ботинки.")
    elif destination.lower() == "лес":
      print("Вы выбрали лес! Отличный выбор для любителей природы. Не забудьте репеллент от насекомых.")
    elif destination.lower() == "пустыня":
      print("Вы выбрали пустыню! Не забудьте взять много воды и головной убор.")
    else:
      print("Некорректный ввод направления.")
  elif vacation_type.lower() == "расслабляющий":
    destination = input("Выберите направление: пляж, SPA или гостиница? ")
    if destination.lower() == "пляж":
      print("Вы выбрали отдых на пляже. Не забудьте взять солнцезащитный крем!")
    elif destination.lower() == "spa":
      print("Вы выбрали отдых в SPA. Побалуйте себя расслабляющими процедурами!")
    elif destination.lower() == "гостиница":
      print("Вы выбрали отдых в гостинице. Отдохните и наслаждайтесь комфортом!")
    else:
      print("Некорректный ввод направления.")
  else:
    print("Некорректный выбор типа отдыха.")
